import java.util.*;
import java.lang.*;

/**
 * Головний клас, що містить демонстрацію математичних операцій.
 * Ця програма демонструє базові арифметичні операції та умовну логіку.
 * 
 */
public class Main {
    /**
     * Головна точка входу в програму.
     * Виконує серію математичних операцій та виводить результат.
     *
     * @param args Аргументи командного рядка (не використовуються в цій програмі)
     */
    public static void main(String[] args) {
        // Ініціалізуємо перше значення
        double Number = 20;
        
        // Ініціалізуємо друге значення
        double Number2 = 80;
        
        // Виконуємо додавання та множення: (20 + 80) * 100 = 10 000
        double Number3 = (Number + Number2) * 100;
        
        // Обчислюємо залишок від ділення на 40: 10 000 % 40 = 0
        double Number4 = Number3 % 40;
        
        // Перевіряємо, чи залишок дорівнює нулю
        boolean Number5 = (Number4 == 0);
        
        // Виводимо результат на основі булевої умови
        if (Number5) {
            System.out.println("Yes"); // Виведе, якщо залишок дорівнює 0
        } else {
            System.out.println("No"); // Виведе, якщо залишок не дорівнює 0
        }
    }
}